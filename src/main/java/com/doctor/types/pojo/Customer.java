package com.doctor.types.pojo;

public class Customer {

	private int customerId;
	private String CustAdharNumber;
	private String custMobileNumber;
	private String custEmail;
	private String custName;
	private String custAddress;
	private int custZip;

	/**
	 * @return the customerId
	 */
	public int getCustomerId() {
		return customerId;
	}

	/**
	 * @param customerId
	 *            the customerId to set
	 */
	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}

	/**
	 * @return the custAdharNumber
	 */
	public String getCustAdharNumber() {
		return CustAdharNumber;
	}

	/**
	 * @param custAdharNumber
	 *            the custAdharNumber to set
	 */
	public void setCustAdharNumber(String custAdharNumber) {
		CustAdharNumber = custAdharNumber;
	}

	/**
	 * @return the custMobileNumber
	 */
	public String getCustMobileNumber() {
		return custMobileNumber;
	}

	/**
	 * @param custMobileNumber
	 *            the custMobileNumber to set
	 */
	public void setCustMobileNumber(String custMobileNumber) {
		this.custMobileNumber = custMobileNumber;
	}

	/**
	 * @return the custEmail
	 */
	public String getCustEmail() {
		return custEmail;
	}

	/**
	 * @param custEmail
	 *            the custEmail to set
	 */
	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}

	/**
	 * @return the custName
	 */
	public String getCustName() {
		return custName;
	}

	/**
	 * @param custName
	 *            the custName to set
	 */
	public void setCustName(String custName) {
		this.custName = custName;
	}

	/**
	 * @return the custAddress
	 */
	public String getCustAddress() {
		return custAddress;
	}

	/**
	 * @param custAddress
	 *            the custAddress to set
	 */
	public void setCustAddress(String custAddress) {
		this.custAddress = custAddress;
	}

	/**
	 * @return the custZip
	 */
	public int getCustZip() {
		return custZip;
	}

	/**
	 * @param custZip
	 *            the custZip to set
	 */
	public void setCustZip(int custZip) {
		this.custZip = custZip;
	}

}
